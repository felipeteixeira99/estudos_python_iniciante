# Obter um tipo de dados
type(valor)

x = str
print(type(x))

# Texto
x = 'Hello Word'
print(x)
print(type(x))

# Inteiro
x = 20
print(x)
print(type(x))

# Ponto flutuante
x = 20.5
print(x)
print(type(x))

# Complex ou complexo
x = 1j
print(x)
print(type(x))

# Lista
x = ['apple', 'orange', 'orange']
print(x)
print(type(x))

# Tupla
x = ('apple', 'banana', 'orange')
print(x)
print(type(x))

# Range
x = range(6)
print(x)
print(type(x))

# Dicionario
x = {"name": "John", "age": 36}
print(x)
print(type(x))

# Set
x = {"aplle", "banana", "morango"}
print(x)
print(type(x))

# Frozenset
x = frozenset({"apple", "banana", "morango"})
print(x)
print(type(x))

# Boolean
x = True
print(x)
print(type(x))

# byte
x = b"Hello"
print(x)
print(type(x))

# Bytearray
x = bytearray(5)
print(x)
print(type(x))

# Memory View
x = memoryview(bytearray(5))
print(x)
print(type(x))

# None Type
x = None
print(x)
print(type(x))

